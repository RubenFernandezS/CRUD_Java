    //Método que limpia los campos para agregar una persona nueva
    public void LimpiarCampos(){
        txtNombre.setText("");
        cbxNacionalidad.setSelectedIndex(0);
        txtEdad.setText("");
        chxCovid.setSelected(false);
        chxVacunacion.setSelected(false);
    }

    //Método que carga los datos para ser editados
    private void CargarDatosParaEditar(Persona persona) {
        txtNombreEditar.setText(persona.getNombre());
        cbxNacionalidadEditar.setSelectedItem(persona.getNacionalidad());
        txtEdadEditar.setText(String.valueOf(persona.getEdad()));
        chxCovidEditar.setSelected(persona.isVacunacionCovid19());
        chxVacunacionEditar.setSelected(persona.isVacunaInflueza());
    }
    
