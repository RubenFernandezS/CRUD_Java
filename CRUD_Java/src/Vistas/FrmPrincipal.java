    private void btnAgregar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregar2ActionPerformed
        //Si la información ingresada es correcta se agrega la nueva persona a la lista
        if(metodos.ValidarNombre(txtNombre.getText()) && metodos.ValidarNacionalidad(String.valueOf(cbxNacionalidad.getSelectedItem())) && metodos.ValidarEdad(txtEdad.getText().trim())){
            Persona nueva = new Persona(Integer.parseInt(lblID.getText()), txtNombre.getText(), cbxNacionalidad.getSelectedItem().toString(),
                    Integer.parseInt(txtEdad.getText()), chxCovid.isSelected(), chxVacunacion.isSelected());

            if (JOptionPane.showConfirmDialog(null, "Persona ingresada exitosamente.\nDesea ingresar otra persona?", "Agregar Personas",0,1)
                    == JOptionPane.YES_OPTION){
                lblID.setText(String.valueOf(++id));
            }else{
                lblTitulo.setText("Bienvenid@");
                cardLayout.show(pnlCardLayout, "pnlBienvenida");
                id++;
            }
            metodos.AgregarPersonas(nueva);
            LimpiarCampos();
        }      
    }//GEN-LAST:event_btnAgregar2ActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        LinkedList<Persona> listaPersonas = metodos.ObtenerLista();
        Iterator<Persona> itr = listaPersonas.iterator();
        
        if (!txtID.getText().isEmpty()) {
            Persona personaBuscada = metodos.Buscar(listaPersonas, itr, Integer.parseInt(txtID.getText().trim()));
                         
            if(personaBuscada != null){
                OcultarMostrarCampos(true);
                CargarDatosParaEditar(personaBuscada);
                lblTitulo.setText("Editar Personas");
                lblBuscarEditar.setText("Ingrese los datos que desea modificar");
            }
            else{
                JOptionPane.showConfirmDialog(null, "El ID: " + txtID.getText() + " no se encuentra", "Buscar Personas",-1,0);
                txtID.setText("");
            }
        }else{
            JOptionPane.showConfirmDialog(null, "Debe ingresar un ID", "Buscar Personas",-1,0);
        } 
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(metodos.Guardar(Integer.parseInt(txtID.getText()),txtNombreEditar.getText(),txtEdadEditar.getText().trim(),
                String.valueOf(cbxNacionalidadEditar.getSelectedItem()),chxCovidEditar.isSelected(),chxVacunacionEditar.isSelected())){
            
            if (JOptionPane.showConfirmDialog(null, "Persona modificada correctamente.\nDesea modificar otra?", "Editar Personas", 0, 1)
                    == JOptionPane.YES_OPTION) {
                LimpiarCampos();
                lblTitulo.setText("Buscar Personas");
                lblBuscarEditar.setText("Ingrese el ID de la persona que desea buscar");
            } else {
                lblTitulo.setText("Bienvenid@");
                cardLayout.show(pnlCardLayout, "pnlBienvenida");
            }
            OcultarMostrarCampos(false); 
        }
        txtID.setText("");
    }//GEN-LAST:event_btnGuardarActionPerformed

